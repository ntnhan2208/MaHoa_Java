    //Tạo sự kiện cho nút mã hoá
    private void btnmahoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmahoaActionPerformed
        String plainText=txtData.getText();//Bắt text khi nhập vào plaintext
        String khoa=txtKey.getText();//Bắt text khi nhập vào key của palintext
        byte[] d1a=new UtilFile().read(plainText);
        String d1b=new String(d1a);
        byte[] d1c=new UtilFile().read(khoa);
        String d1d=new String(d1c);
        int key;//Khai báo biến key
        key=Integer.parseInt(d1d);//ép kiểu key thành dạng số
        if(key<0)
        {
            key=26-(-key%26);//tính key trường hợp key <0
        }
        String kq="";//khởi tạo biến kq
        for(int i=0; i<d1b.length();i++)
        {
            char ch=mahoa(d1b.charAt(i),key);//mã hoá
            kq=kq+ch;
        }
        Path path = Paths.get("C:\\temp\\FileDaDuocMaHoa_Ceasar.txt");
                    try {
                        Files.write(path, kq.getBytes());
                    } catch (IOException ex) {
                        Logger.getLogger(frmCeasar.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    JOptionPane.showMessageDialog(this, "File đã mã hoá được lưu ở C:\\temp\\ ",
                                    "Thông báo", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnmahoaActionPerformed
    
