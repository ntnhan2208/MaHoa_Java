    //mã hoá: mahoa(ch,k)=(ch+k)mod26 với ch là chỉ số của các ký tự cần mã hoá
    private char mahoa(char ch, int key) 
    {
        if(Character.isLetter(ch)){
            ch=(char)('a'+(ch-'a'+key)%26);
        }
        return ch;
    }
    //giải mã: giaima(ch,k)=(ch-k)mod26 với ch là chỉ số của các ký tự cần giải mã
    private char giaima(char ch, int key)
    {
        if(Character.isLetter(ch)){
             ch=(char)('a'+(ch-'a'-key)%26);
        }
        return ch;
    }
